func SplitImage(image image.Image, w, h, x0, y0 int) []byte {
	b := make([]byte, w*h)
	for x := 0; x < w; x++ {
		for y := 0; y < h; y++ {
			r, _, _, _ := image.At(x+x0, y+y0).RGBA()
			if r > 0 {
				r = 1
			}
			b[x+y*w] = byte(r)
		}
	}
	return b
}

