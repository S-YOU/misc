func IsSameImage(a, b []byte) bool {
	for i := 0; i < len(a); i++ {
		if a[i] != b[i] {
			return false
		}
	}
	return true
}

func FindNum(tbl [][]byte, a []byte) int {
	for i := 0; i < 16; i++ {
		if IsSameImage(tbl[i], a) {
			return i
		}
	}
	return -1
}

func ReadBinary(tbl [][]byte, name string) (r []byte) {
	image := GetPngData(name)
	gw := image.Bounds().Max.X
	gh := image.Bounds().Max.Y
	for y := 12; y < gh; y += 22 {
		for x := 9; x < gw; x += 36 {
			idx := FindNum(tbl, SplitImage(image, W, H, x, y))
			if idx >= 0 {
				r = append(r, byte(idx))
			}
			idx = FindNum(tbl, SplitImage(image, W, H, x+12, y))
			if idx >= 0 {
				r = append(r, byte(idx))
			}
		}
	}
	return
}

